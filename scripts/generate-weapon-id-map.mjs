// Generate Weapon ID map from Enka itemId → project weapon database id
import { createRequire } from 'module';
const require = createRequire(import.meta.url);
const genshindb = require('genshin-db');
import * as fs from 'fs';
import * as path from 'path';

// Load our weapon database to match zhNames
const allWeaponsJson = JSON.parse(
  fs.readFileSync(
    path.resolve(import.meta.dirname, '..', 'src', 'data', 'weapons', '_all.json'),
    'utf-8'
  )
);

// Build zhName → projectId index
const zhNameToId = new Map();
for (const w of allWeaponsJson) {
  if (w?.nameZh) zhNameToId.set(w.nameZh, w.id);
}

console.log(`Project weapon database: ${zhNameToId.size} entries`);

// Get all weapons from genshin-db
const allNames = genshindb.weapons('names', { matchCategories: true });
console.log(`genshin-db weapons: ${allNames.length} entries`);

const mapping = {};
let matched = 0;
const unmatchedList = [];

for (const name of allNames) {
  const en = genshindb.weapons(name, { resultLanguage: 'English' });
  const zh = genshindb.weapons(name, { resultLanguage: 'ChineseSimplified' });
  
  if (!en?.id) continue;
  
  const enkaItemId = String(en.id);
  const zhName = zh?.name || name;
  let projectId = zhNameToId.get(zhName);
  
  if (!projectId) {
    // Try case-insensitive match
    for (const [dbZh, dbId] of zhNameToId) {
      if (dbZh.toLowerCase() === zhName.toLowerCase()) {
        projectId = dbId;
        break;
      }
    }
  }
  
  if (projectId) {
    mapping[enkaItemId] = { projectId, zhName };
    matched++;
  } else {
    unmatchedList.push({ enkaItemId, zhName, genshinName: name });
  }
}

console.log(`Matched: ${matched}, Unmatched: ${unmatchedList.length}`);
for (const u of unmatchedList.slice(0, 15)) {
  console.log(`  UNMATCHED: ${u.enkaItemId} → "${u.zhName}" (genshin-db: ${u.genshinName})`);
}

// Build output
let output = `// Auto-generated — do not edit manually.
// Maps Enka weapon itemId → project weapon database id.
// Generated by scripts/generate-weapon-id-map.mjs

export const WEAPON_ID_MAP: Record<string, { projectId: string; zhName: string }> = {
`;

// Sort by itemId
const sorted = Object.entries(mapping).sort(([a], [b]) => parseInt(a) - parseInt(b));
for (const [itemId, info] of sorted) {
  output += `  '${itemId}': { projectId: '${info.projectId}', zhName: '${info.zhName}' },\n`;
}
output += '};\n';

const outPath = path.resolve(import.meta.dirname, '..', 'src', 'data', 'weapon_id_map.ts');
fs.writeFileSync(outPath, output);
console.log(`Written ${sorted.length} entries to ${outPath}`);
